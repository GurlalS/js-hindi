// switch case concept =>. alternate to if elseif else

// syntax
// switch (key) {
//     case value:
        
//         break;

//     default:
//         break;
// }

const month = "march"

switch (month) {
    case "jan":
        console.log("January");
        break;
    case "feb":
        console.log("feb");
        break;
    case "march":
        console.log("march");
        break;
    case "april":
        console.log("april");
        break;

    default:
        console.log("default case match");
        break;
}


// Note: if break is is missed in any case and case case got executed, it will execute rest all the cases until it encounters break or default. default will not get executed